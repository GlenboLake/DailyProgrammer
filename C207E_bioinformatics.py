pairs = {'A': 'T', 'T': 'A', 'C': 'G', 'G': 'C'}


def complement(dna):
    return ''.join(pairs[base] if base in pairs else base for base in dna)

dna = 'A A T G C C T A T G G C'
print(dna)
print(complement(dna))

